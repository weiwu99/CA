package cellsociety.control;

/**
 * SimKey enum is necessary to order the map retrieved from the current simulation. The map contains
 * the keys (type.. title.. etc.) and the string values associated with them.
 *
 * @author Luke Turkovich
 */
public enum SimKey {
  Type,
  Title,
  Author,
  Description,
  InitialStates,
  StateColors
}